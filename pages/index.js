import { useState, useEffect, useCallback } from "react";
import Head from "next/head";
import Banner from "../components/Banner";
import Header from "../components/Header";
import Card from "../components/UI/Card";

export default function Home() {
  const [enabled, setEnabled] = useState(false);
  const [jobData, setJobData] = useState([]);

  const fetchJobsHandler = useCallback(async () => {
    const response = await fetch("data.json");
    const data = await response.json();

    const transformedJobsData = data.map((job) => {
      return {
        id: job.id,
        company: job.company,
        logo: job.logo,
        position: job.position,
        postedAt: job.postedAt,
        logoBackground: job.logoBackground,
        contract: job.contract,
        location: job.location,
      };
    });

    setJobData(transformedJobsData.slice(0, 12));
  }, []);

  useEffect(() => {
    fetchJobsHandler();
  }, [fetchJobsHandler]);

  return (
    <div className={`${enabled && `dark`}`}>
      <div className='h-full bg-lightgray transition-colors duration-300 ease-in-out dark:bg-midnight'>
        <Head>
          <title>devjobs</title>
          <meta name='description' content='Generated by create next app' />
          <link rel='icon' href='/assets/favicon-32x32.png' />
        </Head>
        <Banner />
        <Header enabled={enabled} setEnabled={setEnabled} />
        {jobData.length > 0 && (
          <main className='mx-6 mt-14 grid sm:mx-auto sm:mt-[70px] sm:max-w-[1190px] sm:grid-cols-2 sm:gap-x-3 sm:px-10 md:mt-[105px] md:grid-cols-3'>
            {jobData.map((job) => {
              return <Card key={job.id} jobData={job} />;
            })}
          </main>
        )}
      </div>
    </div>
  );
}
